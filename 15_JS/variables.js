// variable declaration using var keywords
var x = 100
var y = 500

console.log(x+y)

// but after the 2015 version var is outdated and introduced let and const

// variable declaration using let keywords
let num1 = 100
let num2 = 100
console.log(num1+num2)
//  update the value of num1 
num1 = 1500
console.log(num1+num2)


// variable declaration using let keywords
const num4 = 100
const num5 = 150
console.log(num5+num4)
//  update the value of num1 
num4 = 1500
console.log(num5+num4)

// in result: const variable's value can not be updated